#include "3-calc.h"

/**
 * op_add - sum of two int.
 * @a: 1st int.
 * @b: 2cond int.
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sum of two int.
 * @a: 1st int.
 * @b: 2cond int.
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication of two int.
 * @a: 1st int.
 * @b: 2cond int.
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two int.
 * @a: 1st int.
 * @b: 2cond int.
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod. of two int.
 * @a: 1st int.
 * @b: 2cond int.
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
