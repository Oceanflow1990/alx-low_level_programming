#include "3-calc.h"

/**
 * op_add - sum two num
 * @a: first num
 * @b: second nu
 *
 * Return: sum of the two nums.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two num
 * @a: first num
 * @b: second nu
 *
 * Return: difference of the two nums.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two num
 * @a: first num
 * @b: second nu
 *
 * Return: product of the two nums.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division two num
 * @a: first num
 * @b: second nu
 *
 * Return: difference of the two nums.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - reminder two num
 * @a: first num
 * @b: second nu
 *
 * Return: reminder of the two nums.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
