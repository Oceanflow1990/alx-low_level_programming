#include <stdio.h>
/**
 * add - add two int.
 * @a: int 1
 * @b: int 2
 *
 * Return: addition of int 1 & int 2
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substract two int.
 * @a: int 1
 * @b: int 2
 *
 * Return: sub of int 1 & int 2
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two int.
 * @a: int 1
 * @b: int 2
 *
 * Return: mul of int 1 & int 2
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - mod two int.
 * @a: int 1
 * @b: int 2
 *
 * Return: mod of int 1 & int 2
 */

int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - divide two int.
 * @a: int 1
 * @b: int 2
 *
 * Return: div of int 1 & int 2
 */

int div(int a, int b)
{
	return (a / b);
}
